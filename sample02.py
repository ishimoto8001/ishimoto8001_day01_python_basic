# 変数
# 数値を変数で管理する
# 値を変数に代入する（assign a value to a variable）
a = 3  # 変数a に 3 を代入する: 3というデータに対して「a」という名前をつけた
b = 4
print(a + b)

# 意味のある名付けの例
unit_price = 300    # 単価
quantity = 5        # 量

total_price = unit_price * quantity
print(total_price)  # 1500

